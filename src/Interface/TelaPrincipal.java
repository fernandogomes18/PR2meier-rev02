   public void ListarPacientes(){
        CadastroPaciente cadPaciente= new CadastroPaciente();
        ArrayList<CadastroPaciente> dadospaciente = new ArrayList<>();
        dadospaciente = cadPaciente.ExibirPaciente();
        cbPaciente.removeAllItems();
        for (int i=0; i <dadospaciente.size(); i++){
            cbPaciente.addItem(dadospaciente.get(i).getCpf());
   
        }
   }
