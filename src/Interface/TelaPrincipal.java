    public void exibirDadosPaciente(javax.swing.JTable table)
    {
        CadastroPaciente paciente = new CadastroPaciente();
        ArrayList<CadastroPaciente> exibirInfo =  paciente.ExibirPaciente();
        DefaultTableModel md = (DefaultTableModel) this.tbDadosPaciente.getModel();
        md.setNumRows(0);
        for (int i=0; i < exibirInfo.size(); i++)
        {
             md.addRow(new Object[]{exibirInfo.get(i).getCpf(),exibirInfo.get(i).getNome(),exibirInfo.get(i).getTel(),exibirInfo.get(i).getEndereco()});
        }
    }

