    private void jBAtualizarQTNActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jBAtualizarQTNActionPerformed
        Consulta c = new Consulta();
        ArrayList<ConsultaCount> cCount = c.consultaCount();
        DefaultTableModel md = (DefaultTableModel) this.tbQuantConsultas.getModel();
        md.setNumRows(0);
        int totalConsultas = 0;
        for (int i=0; i < cCount.size(); i++)
        {
             md.addRow(new Object[]{cCount.get(i).crm, cCount.get(i).nome, cCount.get(i).qnt});
             totalConsultas += Integer.parseInt(cCount.get(i).qnt);
        }
        jTtotalconsultas.setText(Integer.toString(totalConsultas));
    }//GEN-LAST:event_jBAtualizarQTNActionPerformed

